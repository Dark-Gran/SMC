package com.darkgran.smc.play;

public class WallInfo extends PositionInfo {
    private float width;
    private float height;
    private float angle;

    public WallInfo() { }


    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return height;
    }

    public float getAngle() {
        return angle;
    }
}
