package com.darkgran.smc.play;

public class WallInfo {
    private float x;
    private float y;
    private float width;
    private float height;
    private float angle;

    public WallInfo() { }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return height;
    }

    public float getAngle() {
        return angle;
    }
}
