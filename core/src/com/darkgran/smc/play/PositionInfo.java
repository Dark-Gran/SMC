package com.darkgran.smc.play;

public class PositionInfo {
    private float x;
    private float y;
    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
