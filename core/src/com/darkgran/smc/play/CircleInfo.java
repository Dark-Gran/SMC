package com.darkgran.smc.play;

public class CircleInfo {
    private ColorType type;
    private float x;
    private float y;
    private float radius;
    private float angle;

    public CircleInfo() { }

    public ColorType getType() {
        return type;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getRadius() {
        return radius;
    }

    public float getAngle() {
        return angle;
    }
}
